let i = 0;

const container = document.getElementById("img-row");
const leftBtn = document.getElementById("left-arrow");
const rightBtn = document.getElementById("right-arrow");
leftBtn.addEventListener("click", slideright);
rightBtn.addEventListener("click", slideleft);

function slideleft() {
  // console.log("left",i);
  if (i < img.length-1) {
    container.style.transform = "translate(-" + `${300 * (i + 1)}` + "px)";
    container.style.transition = "transform 0.7s ease";
    i++;
  }
}
function slideright() {
  // console.log("right",i);
  if (i > 0) {
    i--;
    container.style.transform = "translate(-" + `${300 * i}` + "px)";
    container.style.transition = "transform 0.7s ease";
  }
}

window.onresize = (e) => {
  console.log(window.innerWidth);
  if (window.innerWidth > 600) {
    container.style.transform = "translate(0px,0px)";
  }
};

